def _fmt_use_case(score_dict, gt_category):
    """Format use case detected cell for report table."""
    uc = score_dict.get("use_case_detected", "unknown")
    # Multi-run: show accuracy rate if available
    if "use_case_correct_rate" in score_dict:
        rate = score_dict["use_case_correct_rate"]
        return f"{uc} ({rate:.0%} correct)"
    correct = score_dict.get("use_case_correct", False)
    tag = "correct" if correct else "incorrect"
    return f"{uc} ({tag})"


def _fmt_citation(score_dict, num_runs=1):
    """Format citation rate cell for report table."""
    rate = score_dict.get("citation_rate", 0.0)
    found = score_dict.get("citations_found", 0)
    total = score_dict.get("total_recommendations", 0)
    if num_runs > 1 and "citation_rate_std" in score_dict:
        std = score_dict["citation_rate_std"]
        return f"{rate:.0%} \u00b1 {std:.0%}"
    return f"{rate:.0%} ({found}/{total} cited)"


def _fmt_metric(score_dict, key, num_runs):
    """Format a numeric metric, showing ± std when multi-run."""
    val = score_dict[key]
    if num_runs > 1 and key + "_std" in score_dict:
        std = score_dict[key + "_std"]
        return f"{val:.0%} \u00b1 {std:.0%}"
    return f"{val:.0%}"


def generate_report(all_results):
    """Build a markdown report string.

    Dynamically adapts columns based on which conditions are present
    (bare, keyword, all-examples, semantic). Shows mean ± std when num_runs > 1.
    """
    num_runs = all_results.get("num_runs", 1)
    base_seed = all_results.get("base_seed", 42)
    temperature = all_results.get("temperature", 0.7)

    # Detect which optional conditions are present
    has_all = any("with_kb_all" in r for r in all_results["queries"])
    has_semantic = any("with_kb_semantic" in r for r in all_results["queries"])

    # Build ordered list of (result_key, column_label)
    conditions = [
        ("without_kb", "Without KB"),
        ("with_kb", "With KB (keyword)"),
    ]
    if has_all:
        conditions.append(("with_kb_all", "With KB (all examples)"))
    if has_semantic:
        conditions.append(("with_kb_semantic", "With KB (semantic)"))

    def _get(r, key):
        """Get condition data with fallback to without_kb."""
        return r.get(key, r["without_kb"])

    # --- Header ---
    lines = [
        "# VEP Assistant Evaluation Results",
        f"\n**Date:** {datetime.datetime.now().isoformat()}",
        f"**Model:** {all_results['model']}",
    ]
    if num_runs > 1:
        seeds = ", ".join(str(base_seed + i) for i in range(num_runs))
        lines.append(f"**Runs per configuration:** {num_runs}")
        lines.append(f"**Seeds:** {seeds}")
    lines.append(f"**Temperature:** {temperature}")
    lines.append(f"**Max tokens:** 4096")
    lines.append("")

    # --- Per-query results ---
    lines.append("## Per-Query Results\n")

    pq_metric_keys = ["enable_precision", "enable_recall", "enable_f1",
                      "disable_precision", "disable_recall", "disable_f1"]
    pq_metric_labels = ["Enable precision", "Enable recall", "Enable F1",
                        "Disable precision", "Disable recall", "Disable F1"]

    for r in all_results["queries"]:
        lines.append(f"### {r['id']}")
        lines.append(f"**Query:** {r['query']}\n")
        if r.get("source") and r["source"] != "synthetic":
            lines.append(f"**Source:** [{r['source']}]({r['source']})\n")
        lines.append(f"**Ground truth use case:** {r['gt_category']}\n")

        col_labels = [label for _, label in conditions]
        lines.append("| Metric | " + " | ".join(col_labels) + " |")
        lines.append("|--------" + "|---" * len(conditions) + "|")

        # Options detected
        cells = []
        for key, _ in conditions:
            d = _get(r, key)
            cells.append(f"{len(d['enabled_found'])} en / {len(d['disabled_found'])} dis")
        lines.append("| Options detected | " + " | ".join(cells) + " |")

        # Numeric metrics
        for mk, ml in zip(pq_metric_keys, pq_metric_labels):
            cells = [_fmt_metric(_get(r, key), mk, num_runs) for key, _ in conditions]
            lines.append(f"| {ml} | " + " | ".join(cells) + " |")

        # Violations
        cells = [str(len(_get(r, key)["species_violations"])) for key, _ in conditions]
        lines.append("| Species violations | " + " | ".join(cells) + " |")
        cells = [str(len(_get(r, key)["conflict_violations"])) for key, _ in conditions]
        lines.append("| Conflict violations | " + " | ".join(cells) + " |")

        # Interpretability
        cells = [_fmt_use_case(_get(r, key), r["gt_category"]) for key, _ in conditions]
        lines.append("| Use case detected | " + " | ".join(cells) + " |")
        cells = [_fmt_citation(_get(r, key), num_runs) for key, _ in conditions]
        lines.append("| Citation rate | " + " | ".join(cells) + " |")

        # Species violation details
        all_sv = [_get(r, key) for key, _ in conditions]
        if any(x["species_violations"] for x in all_sv):
            for key, label in conditions:
                sv = _get(r, key)["species_violations"]
                lines.append(f"\n*Species violations ({label}):* {sv or 'none'}")
        lines.append("")

    # --- Summary ---
    lines.append("## Summary\n")
    n = len(all_results["queries"])

    sum_metric_keys = ["enable_f1", "disable_f1", "enable_precision", "enable_recall"]
    sum_metric_labels = ["Enable F1", "Disable F1", "Enable Precision", "Enable Recall"]

    # Build header: condition columns + delta columns
    header_cells = []
    for _, label in conditions:
        header_cells.append(label)
    # Add delta columns for each non-bare condition vs bare
    for _, label in conditions[1:]:
        short = label.split("(")[1].rstrip(")") if "(" in label else label
        header_cells.append(f"\u0394 ({short} vs bare)")
    lines.append("| Metric | " + " | ".join(header_cells) + " |")
    lines.append("|--------" + "|---" * len(header_cells) + "|")

    for label, metric in zip(sum_metric_labels, sum_metric_keys):
        avgs = {}
        for key, _ in conditions:
            avgs[key] = sum(_get(r, key)[metric] for r in all_results["queries"]) / n
        cells = [f"{avgs[key]:.0%}" for key, _ in conditions]
        for key, _ in conditions[1:]:
            delta = avgs[key] - avgs["without_kb"]
            cells.append(f"{delta:+.0%}")
        lines.append(f"| {label} | " + " | ".join(cells) + " |")

    # Violation totals
    for viol_key, viol_label in [("species_violations", "Species violations (total)"),
                                  ("conflict_violations", "Conflict violations (total)")]:
        totals = {}
        for key, _ in conditions:
            totals[key] = sum(len(_get(r, key)[viol_key]) for r in all_results["queries"])
        cells = [str(totals[key]) for key, _ in conditions]
        for key, _ in conditions[1:]:
            cells.append(f"{totals[key] - totals['without_kb']:+d}")
        lines.append(f"| {viol_label} | " + " | ".join(cells) + " |")

    # Use case accuracy
    uc_totals = {}
    for key, _ in conditions:
        uc_totals[key] = sum(1 for r in all_results["queries"] if _get(r, key).get("use_case_correct"))
    cells = [f"{uc_totals[key]}/{n} ({uc_totals[key]/n:.0%})" for key, _ in conditions]
    for key, _ in conditions[1:]:
        cells.append(f"{uc_totals[key] - uc_totals['without_kb']:+d}")
    lines.append("| Use case accuracy | " + " | ".join(cells) + " |")

    # Citation rate
    cit_avgs = {}
    for key, _ in conditions:
        cit_avgs[key] = sum(_get(r, key).get("citation_rate", 0) for r in all_results["queries"]) / n
    cells = [f"{cit_avgs[key]:.0%}" for key, _ in conditions]
    for key, _ in conditions[1:]:
        cells.append(f"{cit_avgs[key] - cit_avgs['without_kb']:+.0%}")
    lines.append("| Citation rate (avg) | " + " | ".join(cells) + " |")

    lines.append("")
    return "\n".join(lines)


